package Metodos;

public class SomarMetodos {
	
	int somar (int a, int b) {
		return a + b;
	}
	
	int somar (int a, int b, int c) {
		return a + b + c;
	}
	
	double media(double a, double b) {
		return (a+b)/2;
	}
	
	double media (double a, double b, double c) {
		return (a+b+c)/2;
	}
	
}
